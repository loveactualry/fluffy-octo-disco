#### 상위 몇건 조회


- ORACLE
SELECT NAME 
FROM ( SELECT NAME
      FROM ANIMAL_INS
      ORDER BY  DATETIME)
WHERE ROWNUM < 2;

- MYSQL

SELECT NAME
FROM ANIMAL_INS
ORDER BY DATETIME
LIMIT 1


#### 중복 카운트

- ORACLE/MYSQL
SELECT COUNT(DISTINCT NAME)
FROM ANIMAL_INS;

#### 날짜형


- MYSQL
SELECT DATE_FORMAT(DATETIME, '%Y-%m-%d %h:%i:%s') FROM ANIMAL_OUTS;
